/**
 * Union type of all available divider keys.
 * Example: 'type01' | 'type02' | ...
 */
export type DividerType = keyof typeof DIVIDERS;

/**
 * Props for the Divider component.
 * - type: Which divider SVG to render (one of the `DIVIDERS` keys).
 * - flipY: If true, flipYs vertically (useful when reusing the same shape for top/bottom).
 * - fitTo: Provide 'top' or 'bottom' to apply a small vertical adjustment so the
 *          divider sits flush with the section edge.
 * - className: Extra classes for positioning or coloring (e.g., Tailwind 'text-white').
 */
export interface DividerProps {
  type: DividerType;
  flipY?: boolean;
  fitTo?: 'top' | 'bottom';
  className?: string;
}

/**
 * Divider
 * Renders one of the SVG DIVIDERS by key.
 */
export default function Divider({ type, flipY, fitTo, className }: DividerProps) {
  // Compute a small translation to snug the divider to the top or bottom edge.
  const fit = fitTo && (fitTo === 'top' ? 'translate-y-[-3px]' : 'translate-y-[3px]');
  return (
    <div className={`${flipY ? '-scale-y-100' : ''} ${fit} ${className}`}>
      {DIVIDERS[type].svg()}
    </div>
  );
}
